#•	В змінній year збігається рік. Визначте чи є він високосним. Рік буде високосним в двох випадках:
# або він ділиться на 4, але при цьому не ділиться на 100, або ділиться на 400.

year = rand(2500)
if year % 400 == 0 || (year % 4 == 0 && year % 100 != 0)
  puts "рік #{year} високосний"
else
  puts "рік #{year} невисокосний"
end